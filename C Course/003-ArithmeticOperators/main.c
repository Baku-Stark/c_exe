#include <stdio.h>
#include <stdlib.h>

int main()
{
    int x = 5;
    int y = 2;

    printf("Addition: %d \n", x + y);
    printf("Subtraction: %d \n", x - y);
    printf("Multiplication: %d \n", x * y);
    printf("Division: %d \n", x / y);
    printf("Modulus: %d \n", x % y);
    printf("Increment: %d \n", x++);
    printf("Decrement: %d \n", x--);

    return 0;
}
